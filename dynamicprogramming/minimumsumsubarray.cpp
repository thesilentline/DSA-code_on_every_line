int main()
{
    int n;
    cin>>n;
    vector<int> a(n);
    for(int i=0;i<n;i++)
    {
        cin>>a[i];
    }

    int min_so_far=INT_MAX;
    int min_ending=INT_MAX;
    for(int i=0;i<n;i++)
    {
        if(min_ending>0)
        min_ending= a[i];

        else
        min_ending+= a[i];

        min_so_far=min(min_so_far,min_ending);
    }
    cout<<min_so_far<<endl;
}